package model;

public class City {
	private int code;
	private String name;
	private int codeState;

	public City() {
		super();
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCodeState() {
		return codeState;
	}

	public void setCodeState(int codeState) {
		this.codeState = codeState;
	}

}
